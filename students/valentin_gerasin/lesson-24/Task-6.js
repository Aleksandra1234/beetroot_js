
let a = +prompt('Введите первое число');
let b = +prompt('Введите второе число');
let op = prompt('Выберете знак: - + / *');

if (op == '+') {
    let c = a + b
    console.log(c);
} else if (op == '-') {
    let c = a - b
    console.log(c);
} else if (op == '*') {
    let c = a * b
    console.log(c);
} else if (op == '/') {
    let c = a / b
    console.log(c);
}





